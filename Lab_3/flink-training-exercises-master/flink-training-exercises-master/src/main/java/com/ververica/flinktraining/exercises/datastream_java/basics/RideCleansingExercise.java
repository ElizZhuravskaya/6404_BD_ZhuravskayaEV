package com.ververica.flinktraining.exercises.datastream_java.basics;

import com.ververica.flinktraining.exercises.datastream_java.sources.TaxiRideSource;
import com.ververica.flinktraining.exercises.datastream_java.datatypes.TaxiRide;
import com.ververica.flinktraining.exercises.datastream_java.utils.ExerciseBase;
import com.ververica.flinktraining.exercises.datastream_java.utils.MissingSolutionException;
import org.apache.flink.api.common.functions.FilterFunction;
import org.apache.flink.api.java.utils.ParameterTool;
import org.apache.flink.streaming.api.datastream.DataStream;
import org.apache.flink.streaming.api.environment.StreamExecutionEnvironment;

/**
 * The "Ride Cleansing" exercise from the Flink training
 * (http://training.ververica.com).
 * The task of the exercise is to filter a data stream of taxi ride records to keep only rides that
 * start and end within New York City. The resulting stream should be printed.
 *
 * Parameters:
 *   -input path-to-input-file
 *
 */
public class RideCleansingExercise extends ExerciseBase {
	public static void main(String[] args) throws Exception {

		ParameterTool params = ParameterTool.fromArgs(args);
		final String input = params.get("input", ExerciseBase.pathToRideData);

		final int maxEventDelay = 60;       // events are out of order by max 60 seconds
		final int servingSpeedFactor = 600; // events of 10 minutes are served in 1 second

		// set up streaming execution environment
		StreamExecutionEnvironment env = StreamExecutionEnvironment.getExecutionEnvironment();
		env.setParallelism(ExerciseBase.parallelism);

		// start the data generator
		DataStream<TaxiRide> rides = env.addSource(rideSourceOrTest(new TaxiRideSource(input, maxEventDelay, servingSpeedFactor)));

		DataStream<TaxiRide> filteredRides = rides
				// filter out rides that do not start or stop in NYC
				.filter(new NYCFilter());

		// print the filtered stream
		printOrTest(filteredRides);

		// run the cleansing pipeline
		env.execute("Taxi Ride Cleansing");
	}

	private static class NYCFilter implements FilterFunction<TaxiRide> {
		// Границы Нью-Йорка
		private static final double NYC_MIN_LAT = 40.4774;
		private static final double NYC_MAX_LAT = 40.9176;
		private static final double NYC_MIN_LON = -74.2591;
		private static final double NYC_MAX_LON = -73.7004;

		@Override
		public boolean filter(TaxiRide taxiRide) throws Exception {
			// Проверяем, находятся ли начальная и конечная точки поездки в пределах Нью-Йорка
			boolean startInNYC = isInNYC(taxiRide.startLat, taxiRide.startLon);
			boolean endInNYC = isInNYC(taxiRide.endLat, taxiRide.endLon);

			return startInNYC && endInNYC;
		}

		// Метод проверки, находится ли точка в пределах города
		private boolean isInNYC(double lat, double lon) {
			return lat >= NYC_MIN_LAT && lat <= NYC_MAX_LAT &&
					lon >= NYC_MIN_LON && lon <= NYC_MAX_LON;
		}
	}

}
